/**
 * @file 店名からジャンルをローカル推定するユーティリティ。
 *
 * AI や外部 API は使わず、店名に含まれるキーワード／チェーン名から
 * ジャンルを推定する（CLAUDE.md の「ローカル完結」方針）。
 * 推定結果はあくまで初期値で、ユーザーが後から手動編集できる。
 */

/**
 * チェーン店・固有名詞 → ジャンル。
 * 店名にジャンル語が含まれないが、店名自体でジャンルが分かるもの。
 * キーは店名に含まれる目印（`name.includes(キー)` で判定）。
 */
const CHAIN_MAP: Record<string, string> = {
  AFURI: 'ラーメン',
  つじ田: 'ラーメン',
  一福: 'ラーメン',
  春木屋: 'ラーメン',
  暖暮: 'ラーメン',
  サイゼリヤ: 'イタリアン',
  サブウェイ: 'サンドイッチ',
  富士そば: 'そば',
  てんや: '天ぷら',
  ミスタードーナツ: 'カフェ',
};

/**
 * キーワード（正規表現）→ ジャンル。
 * 店名の表記ゆれを部分一致で吸収する。後から追記しやすいよう1箇所に集約。
 */
const KEYWORD_MAP: Record<string, RegExp> = {
  ラーメン: /ラーメン|らーめん|らー麺|中華そば|つけ麺|つけめん|三田製麺|家系|米線/,
  餃子: /餃子/,
  うどん: /うどん/,
  そば: /そば|蕎麦/,
  イタリアン: /パスタ|PASTA|イタリ|トラットリア|ジェンティーレ/i,
  タイ料理: /タイ料理|タイ屋台|タイ・イサーン|イサーン|ガパオ|コンロウ|ヤムヤム|ラオラオ/,
  焼肉: /焼肉|焼き肉|溶岩焼/,
  焼き鳥: /焼き鳥|焼鳥|串焼|串焼き/,
  カフェ: /カフェ|cafe|喫茶|ドーナツ|たいやき|テラス/i,
  カレー: /カレー/,
  海鮮: /海鮮|寿司|鮨|貝|水産|鶏飯|魚/,
  天ぷら: /天丼|天ぷら|天麩羅/,
  スペイン料理: /スペイン|Espana|Taberna|PARADOR/i,
  肉料理: /ステーキ|肉バル|和牛/,
  居酒屋: /居酒屋|酒場|酒/,
  韓国料理: /韓国|ケジャン|チャカンジャン/,
  ベトナム料理: /ベトナム|BANH\s*MI/i,
  インド料理: /インド|TANDOOR/i,
  定食: /食堂|定食|めし処|ごはん/,
};

/**
 * 店名からジャンルを推定する。
 * @param name 店名
 * @returns 推定ジャンル（重複なし）。該当が無ければ空配列。
 */
export function guessGenres(name: string): string[] {
  const found = new Set<string>();

  // 1. チェーン／固有名詞マップ（部分一致）
  for (const [key, genre] of Object.entries(CHAIN_MAP)) {
    if (name.includes(key)) found.add(genre);
  }

  // 2. キーワードマップ（正規表現の部分一致）
  for (const [genre, pattern] of Object.entries(KEYWORD_MAP)) {
    if (pattern.test(name)) found.add(genre);
  }

  return [...found];
}
